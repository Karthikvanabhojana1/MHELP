    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        String labName = txtLabName.getText();
        String labAddr = txtLabAddr.getText();
        String email = txtEmail.getText();
        String password = txtPass.getText();
        String confirmPassword = txtConfirmPass.getText();

        if (labName.isEmpty() || labAddr.isEmpty() || email.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Create a new PharmacyProfile
        LabProfile labProfile = new LabProfile(labName, labAddr, email, password);

        // Add the pharmacy profile to the shared PharmacyDirectory
            business.getLabDirectory().setLab(labProfile);

        // Create a new UserAccount associated with the pharmacy profile
        UserAccountDirectory uadirectory = business.getUserAccountDirectory();
        UserAccount ua = uadirectory.newUserAccount(labProfile, email, password);

        // Display success message
        JOptionPane.showMessageDialog(this, "Laborartory account created successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnSignUpActionPerformed


